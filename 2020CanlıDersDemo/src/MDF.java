
public class MDF {
	
	
	public MDF(int bigMdfWide, int bigMdfLong, int smallMdfwide, int smallMdfLong) {
		super();
		this.bigMdfWide = bigMdfWide;
		this.bigMdfLong = bigMdfLong;
		this.smallMdfwide = smallMdfwide;
		this.smallMdfLong = smallMdfLong;
	}

	public MDF() {
		
	}

	private int bigMdfWide = 366;
	private int bigMdfLong = 200;
	private int smallMdfwide = 230;
	private int smallMdfLong = 145;
	public int getBigMdfWide() {
		return bigMdfWide;
	}

	public void setBigMdfWide(int bigMdfWide) {
		this.bigMdfWide = bigMdfWide;
	}

	public int getBigMdfLong() {
		return bigMdfLong;
	}

	public void setBigMdfLong(int bigMdfLong) {
		this.bigMdfLong = bigMdfLong;
	}

	public int getSmallMdfwide() {
		return smallMdfwide;
	}

	public void setSmallMdfwide(int smallMdfwide) {
		this.smallMdfwide = smallMdfwide;
	}

	public int getSmallMdfLong() {
		return smallMdfLong;
	}

	public void setSmallMdfLong(int smallMdfLong) {
		this.smallMdfLong = smallMdfLong;
	}
	
	
	
	

}
 